// 1 An array is a group of related data elements kept in close proximity to one another in memory.
// 2 The sole way to retrieve each data element directly is by using its index number, making it the most basic data structure.
// 3  Multiple data elements of the same kind are represented by arrays under a single name.

// 4 Using the index number makes it simple to access or search for a specific element within an array.


// 5 By simply increasing the index by  1, it is simple to go across an array.

//6 For all of their data elements, arrays allot memory in contiguous memory regions.

// 7 The array name, the elements, and the element's data type will always be three components shared by all initializations.

